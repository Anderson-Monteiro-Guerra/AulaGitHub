function diaDaSemana(dia){
    if(dia===1){
        console.log('Domigo!')
    } else if(dia===2){
        console.log('Segunda feira!');
    }else if(dia===3){
        console.log('Terça feira!');
    }else if(dia===4){
        console.log('Quarta feira!');
    }else if(dia===5){
        console.log('Quinta feira!');
    }else if(dia===6){
        console.log('Sexta feira!');
    }else{
        console.log('Sábado!');
    }
}
diaDaSemana(1);